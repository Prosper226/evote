1.    Être Nous Citoyens

1.1. Être Nous Citoyens, c’est s’engager sans esprit partisan dans un mouvement politique qui entend rassembler et impliquer tous les citoyens, pour transformer radicalement notre modèle politique, économique et social, en plaçant l’humain au cœur des décisions pour un développement durable [1] dans une société libre, philanthrope [2] et responsable.

1.2. Être Nous Citoyens, c’est privilégier l’esprit d’ouverture et lutter contre les extrémismes, les sectarismes et les dogmatismes en tous genres. C’est militer pour une représentation politique qui reflète la diversité du pays et de ses talents. C’est proposer une vraie alternative aux populismes et promouvoir une société du lien, fière de son Histoire et confiante en ses atouts et en son avenir.

1.3. Être Nous Citoyens, c’est refuser le déni, la facilité, les boucs émissaires et la résignation. C’est être convaincu que la France est une surdouée qui s’ignore. C’est être convaincu qu’il est possible d’en finir avec le chômage de masse, c’est agir pour réformer en profondeur la France en s’appuyant sur notre culture, nos compétences et nos expériences acquises dans la société civile.

1.4. Être Nous Citoyens, c’est lutter contre la sur-dépense qui génère sur-fiscalité, surendettement, suradministration et sous-efficacité. Ces dérives conduisent au final à la perte de souveraineté, au chômage de masse, à l’appauvrissement et au désespoir.

1.5. Être Nous Citoyens, c’est exiger sans plus attendre trois ruptures d’avenir : dans le fonctionnement de la vie politique pour une meilleure représentativité, dans le fonctionnement économique et social pour plus d’efficacité et dans la gestion de la dépense publique pour plus de responsabilité.

1.6. Être Nous Citoyens, c’est avant tout agir pour le bien commun et proposer un modèle de société durable qui réconcilie l’économie, le social et l’écologie tout en étant soucieux des générations futures.

2.    Nos valeurs, Nos attitudes

2.1. Reconnaissance : Nous reconnaissons chaque citoyen comme un être digne, libre et responsable qui s’épanouit dans sa relation à autrui et dans le respect de la planète, à l’intérieur d’une organisation sociale fondée sur la conscience, la bienveillance et l’initiative.

2.2. Philanthrope : Nous considérons comme essentiel de conduire des politiques philanthropiques ambitieuses, ce qui suppose un environnement favorable à la création de richesses et implique un devoir d’efficacité et de responsabilité dans la dépense et les actions publiques. Nous encourageons également les différentes formes d’entraide privée qui méritent autant de reconnaissance que les initiatives de solidarité publiques.

2.3. Ouverture d’esprit et humanisme :

2.3.1) Convaincus de l’idée et de l’ambition européenne, nous sommes attachés à construire une Europe utile, efficace et proche de ses citoyens

2.3.2) Fiers de notre culture française, nous voulons préserver et renforcer une société laïque, républicaine, où l’altérité est une richesse et la diversité une force.

2.3.3) Nous veillons à ce que chaque personne concernée, y compris les plus vulnérables, soit partie prenante des débats et des actions.

2.3.4) Nous militons pour une classe politique vraiment représentative de la diversité de la population française.

2.4. Sens des responsabilités, individuel et collectif :

2.4.1) Nous veillons à la mise en œuvre concrète d’un équilibre des droits et des devoirs.

2.4.2) Nous valorisons l’effort, le travail, l’accès au savoir et la prise de risque, comme moyens d’épanouissement de l’individu et facteurs de progrès social.

2.4.3) Nous croyons en la responsabilité individuelle, ferment nécessaire d’une plus grande responsabilité collective.

2.4.4) Nous inscrivons nos actions dans une vision à long terme et intégrons le respect de l’environnement dans l’ensemble de nos choix.

2.5. Engagement et Esprit d’entreprendre :

2.5.1) Pour tirer parti du potentiel exceptionnel de notre pays, chacun d’entre nous doit pouvoir devenir entrepreneur de sa vie et acteur de la transformation du pays.

2.5.2) Nous recherchons le développement de nos territoires, le rayonnement de la France et de l’Europe. Nous valorisons la création dans tous les domaines.

2.5.3) Nous souhaitons promouvoir de manière concrète et effective l’égalité des chances en permettant à chacun de cultiver ses talents et de voir ses mérites encouragés et reconnus.

2.6. Transparence, éthique et exemplarité :

2.6.1) Nous plaçons l’intérêt de la France avant toute ambition personnelle.

2.6.2) Nous combattons avec force les conflits d’intérêts. Les élus ont un devoir d’exemplarité et tout écart doit être durement sanctionné.

2.6.3) La situation de la France suppose un discours de vérité dans l’analyse, du courage dans l’application des réformes, et des modalités transparentes d’évaluation et de contrôle par le citoyen.

2.6.4) Nous favorisons une démocratie plus ouverte avec un accès large des citoyens aux informations sur le fonctionnement de nos institutions et les résultats des politiques publiques.

2.7. Équité, Philanthropie et Justice :

2.7.1) Nous pensons indispensable pour le fonctionnement harmonieux et efficace de la société d’agir avec humanisme et de porter une attention particulière aux exclus.

2.7.2) L’exercice de la philanthropie et les actions qui en résultent ne sont pas des domaines réservés de l’action publique. Nous voulons favoriser l’émergence d’initiatives privées. Nous combattons les privilèges et les corporatismes pour un accès équitable aux protections sociales et collectives.

2.7.3) Nous reconnaissons la famille, l’école, le monde associatif et l’environnement professionnel comme les lieux fondamentaux du développement des liens sociaux, de la confiance et de la responsabilité

2.7.4) La France doit prendre sa part à l’édification d’un monde de paix et de justice, renforcer les coopérations et favoriser les échanges interculturels.

3. Nos principes d’action

3.1. Faire preuve d’exigence dans le projet, et rechercher l’efficacité dans l’action

3.1.1) Chaque projet doit être porteur d’objectifs, d’une méthode partagée de conception, de mise en œuvre et d’évaluation des résultats

3.1.2) Nous devons avoir le courage de nous attaquer aux sujets difficiles en refusant les tabous et les intérêts particuliers qui nuiraient à l’intérêt général

3.2. Faire confiance a priori, contrôler a posteriori :

3.2.1) La régulation est nécessaire et doit se limiter à la construction d’un cadre garantissant les libertés fondamentales dans lequel les différents acteurs peuvent agir et interagir

3.2.2) Nous favorisons l’innovation dans tous les domaines.

3.2.3) Nous renforçons les fonctions de contrôle, d’évaluation et de diffusion des résultats.

3.3. Décentraliser les pouvoirs de décision pour plus d’efficacité et de proximité avec les citoyens :

3.3.1) Donner aux acteurs privés ou à des agences indépendantes la possibilité de réaliser certaines missions de service public, chaque fois que la collectivité y gagne en efficience et responsabilisation.

3.3.2) Veiller à ce que l'élaboration des solutions, la prise de décision et l’évaluation des réalisations soient mises en œuvre au niveau le plus le plus proche des citoyens chaque fois que cela est possible

3.3.3) Concentrer l’action de l’État sur ses fonctions régaliennes

3.4. Veiller à la simplicité et au pragmatisme de l’action :

3.4.1) En finir avec les visions technocratiques de la réalité et la mise en place d’« usines à gaz » dans le développement des normes

3.4.2) Face à l’inflation législative historique dans notre pays, privilégier l’accord négocié chaque fois que possible.

3.5. Expérimenter et généraliser les bonnes pratiques identifiées :

3.5.1) L’action publique privilégie l’expérimentation, son évaluation systématique avant sa généralisation

3.5.2) L’action publique doit s’inspirer des meilleures pratiques des autres pays pour renforcer les atouts de la France dans la mondialisation

3.5.3) L’action publique encourage l’initiative et accepte le droit à l’erreur et la remise en cause. Elle doit s’inscrire dans une vision de long terme, de manière assumée au niveau politique

3.6. Faire de la politique autrement :

3.6.1) L’engagement politique correspond à une mission de service d’intérêt général et ne s’inscrit pas dans une logique de carrière. Le cumul des mandats doit donc être limité. Les mandats doivent être non reconductibles à l’exception de celui des petites collectivités.

3.6.2) L’action politique allie le temps court de la gestion courante du pays et le temps long nécessaire à la préparation de l’avenir.

3.6.3) L’action politique doit conduire au respect des engagements et de la parole donnée ainsi qu’à la nécessité de rendre compte aux citoyens des mesures prises. Les acteurs politiques ont un devoir de pédagogie.

[1] Le développement durable est un développement qui répond aux besoins du présent sans compromettre la capacité des générations futures à répondre à leurs propres besoins. Il s’agit de trouver l’équilibre entre l’économie, le social et l’écologie

[2] Philanthrope : c’est aller au-delà de la compassion devant la détresse de nos semblables pour chercher les causes de la misère et trouver des solutions pour y remédier. Définition du "Petit Robert" : Un philanthrope est une personne qui s'emploie à améliorer le sort matériel et moral de ses semblables.
